#include <iostream>
using namespace std;
int main() {
	long long z1, z2;
	cin >> z1 >> z2;
	cout << z1 << " + " << z2 << " = " << z1 + z2 << endl;
	cout << z1 << " * " << z2 << " = " << z1 * z2 << endl;
	cout << z1 << " - " << z2 << " = " << z1 - z2 << endl;
	return 0;
}
